function GetWbtcontractAddress() {
  let connectedNetwork = localStorage.getItem('network_connected')
  // console.log('debug running GetWbtcontractAddress --', connectedNetwork)
  let response = ''
  if (process.env.REACT_APP_DEV_ENV === 'production') {
    // response = PROD_WBTC_ADDRESS
    if (connectedNetwork == 'AVALANCHE') {
      response = '0x152b9d0FdC40C096757F570A51E494bd4b943E50'
    } else if (connectedNetwork == 'BOB') {
      response = '0x03c7054bcb39f7b2e5b2c7acb37583e32d70cfa3'
    }
  } else {
    if (connectedNetwork == 'AVALANCHE') {
      response = AVAX_DEV_WBTC_ADDRESS
    } else if (connectedNetwork == 'BOB') {
      response = BOB_DEV_WBTC_ADDRESS
    }
  }
  return response
}

